# 3. 지수표현방식
# 유효숫자e^지수 = 유효숫자X10^지수
a = 1e9 #10의 9제곱 -> 10억
print(a)

# 752.5
a = 75.25e1 # 75.25*10^1 -> 752.5
print(a)

#3.954
a= 3954e-3 # 3954*10^-3 -> 3.954
print(a)
